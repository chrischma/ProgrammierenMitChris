# Prüfen, ob Werte gleich sind
5 == 10

# Prüfen, ob Werte ungleich sind
5 != 10

# Prüfen, ob ein Wert größer ist als ein anderer Wert.
5 > 10

# Prüfen, ob ein Wert kleiner ist als ein anderer Wert.
5 < 10

# Prüfen, ob ein Wert größer oder gleich einem anderen Wert ist.
5 >= 10

# Prüfen, ob ein Wert kleiner oder gleich einem anderen Wert ist.
5 <= 10
